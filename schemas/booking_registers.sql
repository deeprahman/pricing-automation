CREATE OR REPLACE FUNCTION create_booking_register(
    p_id BIGINT DEFAULT NULL,
    p_type TEXT DEFAULT 'booking',
    p_arrival DATE DEFAULT NULL,
    p_departure DATE DEFAULT NULL,
    p_booked_at TIMESTAMPTZ DEFAULT NULL,
    p_guest_id BIGINT DEFAULT NULL,
    p_platform_id INT DEFAULT NULL,
    p_listing_id TEXT DEFAULT NULL,
    p_thread_ids_json JSONB DEFAULT NULL,
    p_metadata JSONB DEFAULT '{}'::JSONB
) RETURNS booking_registers AS $$
DECLARE
    v_lookup RECORD;
    v_metadata JSONB;
    v_row booking_registers%ROWTYPE;
BEGIN
    SELECT *
    INTO v_lookup
    FROM resolve_booking_register_lookup(p_platform_id, p_listing_id);

    v_metadata := enrich_booking_register_stay_metrics(
        normalize_booking_register_metadata(p_metadata, p_listing_id),
        p_arrival,
        p_departure,
        p_booked_at
    );

    IF p_id IS NULL THEN
        INSERT INTO booking_registers (
            type,
            arrival,
            departure,
            booked_at,
            guest_id,
            property_id,
            platform_id,
            ppl_id,
            thread_ids_json,
            metadata
        )
        VALUES (
            COALESCE(NULLIF(BTRIM(p_type), ''), 'booking'),
            p_arrival,
            p_departure,
            p_booked_at,
            p_guest_id,
            v_lookup.property_id,
            p_platform_id,
            v_lookup.ppl_id,
            p_thread_ids_json,
            v_metadata
        )
        RETURNING *
        INTO v_row;
    ELSE
        INSERT INTO booking_registers (
            id,
            type,
            arrival,
            departure,
            booked_at,
            guest_id,
            property_id,
            platform_id,
            ppl_id,
            thread_ids_json,
            metadata
        )
        VALUES (
            p_id,
            COALESCE(NULLIF(BTRIM(p_type), ''), 'booking'),
            p_arrival,
            p_departure,
            p_booked_at,
            p_guest_id,
            v_lookup.property_id,
            p_platform_id,
            v_lookup.ppl_id,
            p_thread_ids_json,
            v_metadata
        )
        RETURNING *
        INTO v_row;
    END IF;

    RETURN v_row;
END;
$$ LANGUAGE plpgsql;


CREATE OR REPLACE FUNCTION update_booking_register(
    p_id BIGINT,
    p_type TEXT DEFAULT NULL,
    p_arrival DATE DEFAULT NULL,
    p_departure DATE DEFAULT NULL,
    p_booked_at TIMESTAMPTZ DEFAULT NULL,
    p_guest_id BIGINT DEFAULT NULL,
    p_platform_id INT DEFAULT NULL,
    p_listing_id TEXT DEFAULT NULL,
    p_thread_ids_json JSONB DEFAULT NULL,
    p_metadata JSONB DEFAULT NULL
) RETURNS booking_registers AS $$
DECLARE
    v_current booking_registers%ROWTYPE;
    v_current_listing_id TEXT;
    v_lookup RECORD;
    v_type TEXT;
    v_arrival DATE;
    v_departure DATE;
    v_booked_at TIMESTAMPTZ;
    v_metadata JSONB;
    v_row booking_registers%ROWTYPE;
BEGIN
    SELECT *
    INTO v_current
    FROM booking_registers
    WHERE id = p_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'booking_registers id % was not found', p_id
            USING ERRCODE = 'P0002';
    END IF;

    SELECT ppl.listing_id
    INTO v_current_listing_id
    FROM platform_property_lookup ppl
    WHERE ppl.id = v_current.ppl_id;

    IF v_current_listing_id IS NULL THEN
        RAISE EXCEPTION
            'platform_property_lookup id % was not found for booking_registers id %',
            v_current.ppl_id, p_id
            USING ERRCODE = '23503';
    END IF;

    SELECT *
    INTO v_lookup
    FROM resolve_booking_register_lookup(
        COALESCE(p_platform_id, v_current.platform_id),
        COALESCE(NULLIF(BTRIM(p_listing_id), ''), v_current_listing_id)
    );

    v_type := COALESCE(NULLIF(BTRIM(p_type), ''), v_current.type);
    v_arrival := COALESCE(p_arrival, v_current.arrival);
    v_departure := COALESCE(p_departure, v_current.departure);
    v_booked_at := COALESCE(p_booked_at, v_current.booked_at);

    IF p_metadata IS NULL THEN
        v_metadata := COALESCE(v_current.metadata, '{}'::JSONB);
    ELSE
        v_metadata := COALESCE(v_current.metadata, '{}'::JSONB)
            || normalize_booking_register_metadata(p_metadata, NULL);
    END IF;

    v_metadata := normalize_booking_register_metadata(
        v_metadata,
        COALESCE(NULLIF(BTRIM(p_listing_id), ''), v_current_listing_id)
    );
    v_metadata := enrich_booking_register_stay_metrics(
        v_metadata,
        v_arrival,
        v_departure,
        v_booked_at
    );

    UPDATE booking_registers
    SET
        type = v_type,
        arrival = v_arrival,
        departure = v_departure,
        booked_at = v_booked_at,
        guest_id = COALESCE(p_guest_id, v_current.guest_id),
        property_id = v_lookup.property_id,
        platform_id = COALESCE(p_platform_id, v_current.platform_id),
        ppl_id = v_lookup.ppl_id,
        thread_ids_json = COALESCE(p_thread_ids_json, v_current.thread_ids_json),
        metadata = v_metadata
    WHERE id = p_id
    RETURNING *
    INTO v_row;

    RETURN v_row;
END;
$$ LANGUAGE plpgsql;


CREATE OR REPLACE FUNCTION upsert_booking_register(
    p_id BIGINT DEFAULT NULL,
    p_type TEXT DEFAULT NULL,
    p_arrival DATE DEFAULT NULL,
    p_departure DATE DEFAULT NULL,
    p_booked_at TIMESTAMPTZ DEFAULT NULL,
    p_guest_id BIGINT DEFAULT NULL,
    p_platform_id INT DEFAULT NULL,
    p_listing_id TEXT DEFAULT NULL,
    p_thread_ids_json JSONB DEFAULT NULL,
    p_metadata JSONB DEFAULT NULL
) RETURNS booking_registers AS $$
DECLARE
    v_external_booking_id TEXT := NULLIF(BTRIM(COALESCE(p_metadata->>'booking_id', '')), '');
BEGIN
    IF p_id IS NULL AND p_platform_id IS NOT NULL AND v_external_booking_id IS NOT NULL THEN
        SELECT br.id
        INTO p_id
        FROM booking_registers br
        WHERE br.platform_id = p_platform_id
          AND br.metadata->>'booking_id' = v_external_booking_id
        ORDER BY br.id DESC
        LIMIT 1;
    END IF;

    IF p_id IS NULL THEN
        BEGIN
            RETURN create_booking_register(
                p_id => NULL,
                p_type => COALESCE(p_type, 'booking'),
                p_arrival => p_arrival,
                p_departure => p_departure,
                p_booked_at => p_booked_at,
                p_guest_id => p_guest_id,
                p_platform_id => p_platform_id,
                p_listing_id => p_listing_id,
                p_thread_ids_json => p_thread_ids_json,
                p_metadata => COALESCE(p_metadata, '{}'::jsonb)
            );
        EXCEPTION
            WHEN unique_violation THEN
                IF p_platform_id IS NOT NULL AND v_external_booking_id IS NOT NULL THEN
                    SELECT br.id
                    INTO p_id
                    FROM booking_registers br
                    WHERE br.platform_id = p_platform_id
                      AND br.metadata->>'booking_id' = v_external_booking_id
                    ORDER BY br.id DESC
                    LIMIT 1;
                END IF;

                IF p_id IS NULL THEN
                    RAISE;
                END IF;
        END;
    END IF;

    BEGIN
        RETURN create_booking_register(
            p_id => p_id,
            p_type => COALESCE(p_type, 'booking'),
            p_arrival => p_arrival,
            p_departure => p_departure,
            p_booked_at => p_booked_at,
            p_guest_id => p_guest_id,
            p_platform_id => p_platform_id,
            p_listing_id => p_listing_id,
            p_thread_ids_json => p_thread_ids_json,
            p_metadata => COALESCE(p_metadata, '{}'::jsonb)
        );
    EXCEPTION
        WHEN unique_violation THEN
            RETURN update_booking_register(
                p_id => p_id,
                p_type => p_type,
                p_arrival => p_arrival,
                p_departure => p_departure,
                p_booked_at => p_booked_at,
                p_guest_id => p_guest_id,
                p_platform_id => p_platform_id,
                p_listing_id => p_listing_id,
                p_thread_ids_json => p_thread_ids_json,
                p_metadata => p_metadata
            );
    END;
END;
$$ LANGUAGE plpgsql;


