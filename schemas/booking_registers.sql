-- ============================================
-- BOOKING REGISTERS TABLE
-- ============================================
-- Run AFTER: property_platform_sql.sql
--
-- Stores all booking data with embedded metadata (JSONB).
--
-- Search paths supported:
--   1. booking entry id           -> id (PRIMARY KEY)
--   2. arrival / departure dates  -> idx_booking_registers_arrival,
--                                    idx_booking_registers_departure,
--                                    idx_booking_registers_scanner (composite)
--   3. ppl_id                     -> idx_booking_registers_ppl_id
--   4. property_id                -> idx_booking_registers_property_id
--   5. platform_id                -> idx_booking_registers_platform_id
--   6. metadata.booking_id        -> idx_booking_registers_meta_booking_id
--   7. metadata.listing_id        -> idx_booking_registers_meta_listing_id
--   8. metadata.bso.cancelled     -> idx_booking_registers_meta_cancelled
--   9. metadata.bso
--        .potential_extension
--        .last_extended           -> idx_booking_registers_meta_last_extended
--   10. Full JSONB metadata search -> idx_booking_registers_metadata_gin
--   11. thread_ids_json            -> idx_booking_registers_thread_ids_gin
-- ============================================

-- ============================================
-- 1) Table definition
-- ============================================
DROP TABLE IF EXISTS booking_registers;

CREATE TABLE IF NOT EXISTS booking_registers (

    -- -- Core identity ------------------------------------------------------
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY
                                    PRIMARY KEY,                 -- booking entry id
    type            TEXT            NOT NULL DEFAULT 'booking',

    -- -- Date range ---------------------------------------------------------
    arrival         DATE            NOT NULL,
    departure       DATE            NOT NULL,

    -- -- Booking facts ------------------------------------------------------
    booked_at       TIMESTAMPTZ     NOT NULL,
    guest_id        BIGINT          NOT NULL,
    property_id     INT             NOT NULL,
    platform_id     INT             NOT NULL,
    ppl_id          BIGINT          NOT NULL,

    -- -- Thread ids (non-empty JSON array enforced below) -------------------
    thread_ids_json JSONB           NOT NULL,

    -- -- Embedded metadata (JSONB object enforced below) --------------------
    --
    --   Expected shape (all keys optional):
    --   {
    --     "booking_id": "<external booking id string>",
    --     "listing_id": "<external listing id string>",
    --     "status":     "booked" | "cancelled" | ...,
    --     NOTE ABOUT status SEMANTICS:
    --       metadata.status is provider-sourced status (e.g., OwnerRez payload).
    --       It is not rewritten by internal operational workflows such as
    --       checkout scanners or remove-bso cleanup steps.
    --       Internal workflow state (for example "checked out" / inactive for
    --       BSO lifecycle decisions) should be tracked under metadata.bso.*
    --       or a separate explicit internal lifecycle field, so provider truth
    --       and internal state remain distinct.
    --     "bso": {
    --       "cancelled": true,
    --       "potential_extension": {
    --         "last_extended": "2026-03-18T14:00:00+00:00"   <- ISO 8601
    --       }
    --     },
    --     "previous": {
    --       "arrival":  ["<date>", ...],
    --       "property": [<id>, ...]
    --     }
    --   }
    metadata        JSONB           NOT NULL DEFAULT '{}'::JSONB,

    -- -- Timestamps ---------------------------------------------------------
    created_at      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    -- -- Foreign key: platform_id must reference a known platform ----------
    CONSTRAINT fk_booking_registers_platform
        FOREIGN KEY (platform_id)
        REFERENCES platforms(id)
        ON DELETE RESTRICT,

    -- -- Foreign key: property_id must reference a known canonical property
    CONSTRAINT fk_booking_registers_property
        FOREIGN KEY (property_id)
        REFERENCES properties(id)
        ON DELETE RESTRICT,

    -- -- Foreign key: ppl_id must reference an exact listing mapping -------
    CONSTRAINT fk_booking_registers_ppl
        FOREIGN KEY (ppl_id)
        REFERENCES platform_property_lookup(id)
        ON DELETE RESTRICT,

    -- -- arrival must be before departure ----------------------------------
    CONSTRAINT chk_booking_registers_dates
        CHECK (arrival < departure),

    -- -- thread_ids_json must be a non-empty JSON array --------------------
    CONSTRAINT chk_booking_registers_thread_ids_non_empty
        CHECK (
            jsonb_typeof(thread_ids_json) = 'array'
            AND jsonb_array_length(thread_ids_json) > 0
        ),

    -- -- metadata must be a JSON object (not array / scalar) ---------------
    CONSTRAINT chk_booking_registers_metadata_object
        CHECK (jsonb_typeof(metadata) = 'object')
);

-- ============================================
-- 2) Standard column indexes
-- ============================================

-- Single-column lookups
CREATE INDEX IF NOT EXISTS idx_booking_registers_arrival
    ON booking_registers (arrival);

CREATE INDEX IF NOT EXISTS idx_booking_registers_departure
    ON booking_registers (departure);

CREATE INDEX IF NOT EXISTS idx_booking_registers_ppl_id
    ON booking_registers (ppl_id);

CREATE INDEX IF NOT EXISTS idx_booking_registers_property_id
    ON booking_registers (property_id);

CREATE INDEX IF NOT EXISTS idx_booking_registers_platform_id
    ON booking_registers (platform_id);

CREATE INDEX IF NOT EXISTS idx_booking_registers_guest_id
    ON booking_registers (guest_id);

CREATE INDEX IF NOT EXISTS idx_booking_registers_updated_at
    ON booking_registers (updated_at);

-- Composite: platform + property (common filter combination)
CREATE INDEX IF NOT EXISTS idx_booking_registers_platform_property
    ON booking_registers (platform_id, property_id);

-- Scanner composite: cursor-based pagination over a date window
CREATE INDEX IF NOT EXISTS idx_booking_registers_scanner
    ON booking_registers (arrival, departure, id);

-- ============================================
-- 3) JSONB -- GIN indexes
-- ============================================

-- Full metadata GIN (covers any ad-hoc JSONB operator search)
CREATE INDEX IF NOT EXISTS idx_booking_registers_metadata_gin
    ON booking_registers USING GIN (metadata);

-- Full thread_ids_json GIN
CREATE INDEX IF NOT EXISTS idx_booking_registers_thread_ids_gin
    ON booking_registers USING GIN (thread_ids_json);

-- ============================================
-- 4) JSONB -- expression indexes for targeted searches
-- ============================================

-- Search by metadata.booking_id
--   WHERE metadata->>'booking_id' = '<value>'
CREATE INDEX IF NOT EXISTS idx_booking_registers_meta_booking_id
    ON booking_registers ((metadata->>'booking_id'))
    WHERE metadata->>'booking_id' IS NOT NULL;

-- Enforce one row per (platform, external booking id) when external id exists.
CREATE UNIQUE INDEX IF NOT EXISTS uq_booking_registers_platform_external_booking_id
    ON booking_registers (platform_id, (metadata->>'booking_id'))
    WHERE metadata->>'booking_id' IS NOT NULL
      AND BTRIM(metadata->>'booking_id') <> '';

-- Search by metadata.listing_id
--   WHERE metadata->>'listing_id' = '<value>'
CREATE INDEX IF NOT EXISTS idx_booking_registers_meta_listing_id
    ON booking_registers ((metadata->>'listing_id'))
    WHERE metadata->>'listing_id' IS NOT NULL;

-- Search by metadata.bso.cancelled = true
--   WHERE (metadata->'bso'->>'cancelled')::BOOLEAN = TRUE
CREATE INDEX IF NOT EXISTS idx_booking_registers_meta_cancelled
    ON booking_registers ((metadata->'bso'->>'cancelled'))
    WHERE metadata->'bso'->>'cancelled' = 'true';

-- Search / filter by metadata.bso.potential_extension.last_extended
--   WHERE (metadata #>> '{bso,potential_extension,last_extended}')::TIMESTAMPTZ ...
CREATE INDEX IF NOT EXISTS idx_booking_registers_meta_last_extended
    ON booking_registers
        ((metadata #>> '{bso,potential_extension,last_extended}'))
    WHERE metadata #>> '{bso,potential_extension,last_extended}' IS NOT NULL;

-- ============================================
-- 5) Lookup helpers
-- ============================================
CREATE OR REPLACE FUNCTION resolve_booking_register_lookup(
    p_platform_id INT,
    p_listing_id TEXT
) RETURNS TABLE (
    ppl_id BIGINT,
    property_id INT
) AS $$
DECLARE
    v_listing_id TEXT := NULLIF(BTRIM(p_listing_id), '');
BEGIN
    IF p_platform_id IS NULL THEN
        RAISE EXCEPTION 'platform_id is required'
            USING ERRCODE = '22023';
    END IF;

    IF v_listing_id IS NULL THEN
        RAISE EXCEPTION 'listing_id is required'
            USING ERRCODE = '22023';
    END IF;

    RETURN QUERY
    SELECT
        ppl.id::BIGINT,
        ppl.properties_ptr::INT
    FROM platform_property_lookup ppl
    WHERE ppl.platform_id = p_platform_id
      AND ppl.listing_id = v_listing_id
    LIMIT 1;

    IF NOT FOUND THEN
        RAISE EXCEPTION
            'platform_property_lookup not found for platform_id=% and listing_id=%',
            p_platform_id, v_listing_id
            USING ERRCODE = '23503';
    END IF;
END;
$$ LANGUAGE plpgsql STABLE;


CREATE OR REPLACE FUNCTION normalize_booking_register_metadata(
    p_metadata JSONB,
    p_listing_id TEXT
) RETURNS JSONB AS $$
DECLARE
    v_metadata JSONB := COALESCE(p_metadata, '{}'::JSONB);
    v_listing_id TEXT := NULLIF(BTRIM(p_listing_id), '');
BEGIN
    IF jsonb_typeof(v_metadata) <> 'object' THEN
        RAISE EXCEPTION 'metadata must be a JSON object'
            USING ERRCODE = '22023';
    END IF;

    IF v_listing_id IS NOT NULL THEN
        v_metadata := jsonb_set(v_metadata, '{listing_id}', to_jsonb(v_listing_id), true);
    END IF;

    RETURN v_metadata;
END;
$$ LANGUAGE plpgsql STABLE;


CREATE OR REPLACE FUNCTION enrich_booking_register_stay_metrics(
    p_metadata JSONB,
    p_arrival DATE,
    p_departure DATE,
    p_booked_at TIMESTAMPTZ
) RETURNS JSONB AS $$
DECLARE
    v_metadata JSONB := COALESCE(p_metadata, '{}'::JSONB);
BEGIN
    IF jsonb_typeof(v_metadata) <> 'object' THEN
        RAISE EXCEPTION 'metadata must be a JSON object'
            USING ERRCODE = '22023';
    END IF;

    IF p_arrival IS NOT NULL AND p_departure IS NOT NULL THEN
        v_metadata := jsonb_set(
            v_metadata,
            '{stay_length}',
            to_jsonb((p_departure - p_arrival)::INT),
            true
        );
    END IF;

    IF p_arrival IS NOT NULL AND p_booked_at IS NOT NULL THEN
        v_metadata := jsonb_set(
            v_metadata,
            '{booking_window}',
            to_jsonb(GREATEST((p_arrival - p_booked_at::DATE)::INT, 0)),
            true
        );
    END IF;

    RETURN v_metadata;
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- ============================================
-- 6) Exact ppl/property mapping validator
--    Validation is based on one exact platform_property_lookup row rather than
--    only checking that some row exists for the same (platform_id, property_id).
-- ============================================
CREATE OR REPLACE FUNCTION validate_booking_registers_ppl()
RETURNS TRIGGER AS $$
DECLARE
    v_lookup_property_id INT;
    v_lookup_platform_id INT;
BEGIN
    SELECT
        ppl.properties_ptr,
        ppl.platform_id
    INTO
        v_lookup_property_id,
        v_lookup_platform_id
    FROM platform_property_lookup ppl
    WHERE ppl.id = NEW.ppl_id;

    IF v_lookup_property_id IS NULL THEN
        RAISE EXCEPTION
            'platform_property_lookup id % was not found',
            NEW.ppl_id
            USING ERRCODE = '23503';
    END IF;

    IF NEW.platform_id IS DISTINCT FROM v_lookup_platform_id THEN
        RAISE EXCEPTION
            'booking_registers.platform_id % does not match platform_property_lookup.platform_id % for ppl_id %',
            NEW.platform_id, v_lookup_platform_id, NEW.ppl_id
            USING ERRCODE = '23514';
    END IF;

    IF NEW.property_id IS DISTINCT FROM v_lookup_property_id THEN
        RAISE EXCEPTION
            'booking_registers.property_id % does not match platform_property_lookup.properties_ptr % for ppl_id %',
            NEW.property_id, v_lookup_property_id, NEW.ppl_id
            USING ERRCODE = '23514';
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_booking_registers_validate_platform_property ON booking_registers;
DROP TRIGGER IF EXISTS trg_booking_registers_validate_ppl ON booking_registers;

CREATE TRIGGER trg_booking_registers_validate_ppl
    BEFORE INSERT OR UPDATE OF property_id, platform_id, ppl_id ON booking_registers
    FOR EACH ROW
    EXECUTE FUNCTION validate_booking_registers_ppl();

