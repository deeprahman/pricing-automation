CREATE OR REPLACE FUNCTION find_booking_registers(
    p_platform_id INT DEFAULT NULL,
    p_metadata_booking_id TEXT DEFAULT NULL,
    p_metadata_listing_id TEXT DEFAULT NULL,
    p_limit INT DEFAULT 100,
    p_cursor BIGINT DEFAULT NULL
) RETURNS SETOF booking_registers AS $$
DECLARE
    v_limit INT := LEAST(GREATEST(COALESCE(p_limit, 100), 1), 500);
BEGIN
    RETURN QUERY
    SELECT *
    FROM booking_registers br
    WHERE (p_platform_id IS NULL OR br.platform_id = p_platform_id)
      AND (p_metadata_booking_id IS NULL OR br.metadata->>'booking_id' = p_metadata_booking_id)
      AND (p_metadata_listing_id IS NULL OR br.metadata->>'listing_id' = p_metadata_listing_id)
      AND (p_cursor IS NULL OR br.id < p_cursor)
    ORDER BY br.id DESC
    LIMIT v_limit;
END;
$$ LANGUAGE plpgsql STABLE;


