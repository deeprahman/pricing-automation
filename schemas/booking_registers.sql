-- ============================================
-- 8) updated_at trigger
--    Only bumps updated_at when a core booking field changes.
--    Metadata-only patches leave updated_at untouched.
-- ============================================
CREATE OR REPLACE FUNCTION set_booking_registers_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    IF  NEW.type            IS DISTINCT FROM OLD.type
     OR NEW.arrival         IS DISTINCT FROM OLD.arrival
     OR NEW.departure       IS DISTINCT FROM OLD.departure
     OR NEW.booked_at       IS DISTINCT FROM OLD.booked_at
     OR NEW.guest_id        IS DISTINCT FROM OLD.guest_id
     OR NEW.property_id     IS DISTINCT FROM OLD.property_id
     OR NEW.platform_id     IS DISTINCT FROM OLD.platform_id
     OR NEW.ppl_id          IS DISTINCT FROM OLD.ppl_id
     OR NEW.thread_ids_json IS DISTINCT FROM OLD.thread_ids_json
    THEN
        NEW.updated_at := NOW();
    ELSE
        NEW.updated_at := OLD.updated_at;   -- metadata-only update -> no bump
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_booking_registers_updated_at ON booking_registers;

CREATE TRIGGER trg_booking_registers_updated_at
    BEFORE UPDATE ON booking_registers
    FOR EACH ROW
    EXECUTE FUNCTION set_booking_registers_updated_at();

